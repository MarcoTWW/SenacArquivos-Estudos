linha = 0               # O valor da variável 'linha' é '0'
for i in range(5,11):   # Para 'i' no intervalo de '5' até '11' (excluindo o 11):
        linha += i      # Soma o valor de 'i' à variável 'linha'. (linha + 5, linha + 6, linha + 7...)
print(linha)            # Escreva o valor da varíavel 'linha', que no momento vale '45'